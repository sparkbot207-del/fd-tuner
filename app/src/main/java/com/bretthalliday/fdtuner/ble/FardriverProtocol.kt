    /**
     * Compute and write CRC into the last 2 bytes of [data] for [length] bytes total.
     * Seed: a=0x3C, b=0x7F (Fardriver-specific initialization).
     */
    fun computeCRC(data: ByteArray, length: Int) {
        var a = 0x3C
        var b = 0x7F
        for (pos in 0 until length - 2) {
            val i = (a xor data[pos].toInt()) and 0xFF
            a = (b xor crcTableHi[i]) and 0xFF
            b = crcTableLo[i] and 0xFF
        }
        data[length - 2] = a.toByte()
        data[length - 1] = b.toByte()
    }

    /**
     * Build an 8-byte write packet.
     *  B0: 0xAA
     *  B1: 0x46  (compute_length=6, flags=1 → 0x40|0x06)
     *  B2: addr (word address)
     *  B3: addr (repeated)
     *  B4-5: value little-endian int16
     *  B6-7: CRC
     */
    fun buildWritePacket(addr: Int, value: Int): ByteArray {
        val pkt = ByteArray(8)
        pkt[0] = PACKET_MAGIC.toByte()
        pkt[1] = 0x46.toByte()
        pkt[2] = (addr and 0xFF).toByte()
        pkt[3] = (addr and 0xFF).toByte()
        pkt[4] = (value and 0xFF).toByte()
        pkt[5] = ((value shr 8) and 0xFF).toByte()
        computeCRC(pkt, 8)
        return pkt
    }

    /**
     * Build the "data gather" command: write 0x8806 to word 0xA0.
     * This triggers the controller to start streaming status packets.
     */
    fun buildDataGatherPacket(): ByteArray = buildWritePacket(0xA0, 0x8806)

    /**
     * Parse a 16-byte status packet from the controller.
     * Returns a map of (word_address → word_value) for all 6 words in the block,
     * or null if the packet is invalid (wrong magic, unknown id, or bad length).
     *
     * Packet layout:
     *  B0:    0xAA (magic)
     *  B1:    id[5:0] = message id, flags[7:6]
     *  B2-13: 12 bytes of data (6 × int16 little-endian words)
     *  B14-15: CRC
     */
    fun parseStatusPacket(raw: ByteArray): Map<Int, Int>? {
        if (raw.size < 16) return null
        if (raw[0].toInt() and 0xFF != PACKET_MAGIC) return null

        val id = raw[1].toInt() and 0x3F
        if (id >= flashReadAddr.size) return null

        val baseAddr = flashReadAddr[id]
        val result = mutableMapOf<Int, Int>()

        // 6 words, each 2 bytes little-endian, starting at raw[2]
        for (wordOffset in 0 until 6) {
            val byteIdx = 2 + wordOffset * 2
            val lo = raw[byteIdx].toInt() and 0xFF
            val hi = raw[byteIdx + 1].toInt() and 0xFF
            val wordValue = (hi shl 8) or lo
            val wordAddr = baseAddr + wordOffset
            result[wordAddr] = wordValue
        }

        return result
    }

    /**
     * Decode live telemetry fields from the raw param map.
     * Word addresses come from spec: 0xE2 block (id=0,7,14,21,28,35,42,48).
     *
     * RPM note: FarDriver stores ALL RPM values as raw = mechanical_RPM × 4.
     * This is a fixed protocol constant (NOT eRPM). Divide raw by 4 for mechanical RPM.
     * polePairs is kept in the signature for API compatibility but is NOT used here.
     */
    fun decodeTelemetry(
        rawParams: Map<Int, Int>,
        @Suppress("UNUSED_PARAMETER") polePairs: Int,
        wheelCircumferenceMm: Int,
        useMph: Boolean
    ): TelemetryData {
        val voltageRaw = rawParams[0xE2] ?: 0
        val voltage = voltageRaw / 10.0f

        val lineCurrentRaw = rawParams[0xE3] ?: 0
        val lineCurrent = lineCurrentRaw.toShort() / 10.0f

        val rpmRaw = rawParams[0xE5] ?: 0
        // raw = mechanical_RPM × 4 → convert to mechanical RPM for display
        val mechanicalRpm = rpmRaw / 4

        val gearRaw = rawParams[0xE6] ?: 0
        val gear = gearRaw and 0xFF

        val controllerTempRaw = rawParams[0xE8] ?: 0
        val controllerTemp = (controllerTempRaw and 0xFF).toByte().toInt()

        val motorTempRaw = rawParams[0xE9] ?: 0
        val motorTemp = (motorTempRaw and 0xFF).toByte().toInt()

        val socRaw = rawParams[0xEA] ?: 0
        val soc = socRaw and 0xFF

        // Speed: raw / 4 = mechanical_RPM; wheel speed = mechanical_RPM × circumference
        val speedKmh = mechanicalRpm * (wheelCircumferenceMm.toDouble() / 1000.0) * 60.0 / 1000.0
        val speed = if (useMph) (speedKmh * 0.621371).toFloat() else speedKmh.toFloat()

        val gearName = when (gear) {
            0 -> "N"
            1 -> "L"
            2 -> "M"
            3 -> "H"
            else -> "?"
        }

        return TelemetryData(
            voltage = voltage,
            lineCurrent = lineCurrent,
            rpm = mechanicalRpm,
            gear = gearName,
            controllerTemp = controllerTemp,
            motorTemp = motorTemp,
            soc = soc,
            speed = speed,
            speedUnit = if (useMph) "mph" else "km/h"
        )
    }
}
